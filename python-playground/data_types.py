# Integers

print("Integer: ")
print(1)
print(type(1))
print(type(1 + 3))

#

# Floats

print("\nFloats: ")
print(1.2)
print(1.2 + 1.4)
print(type(1.0))
print(.4)
print(.4e2)
print(.4e-2)

# Strings

print("\nStrings: ")
print("I am a string with double quotes")
print(type("I am a string with double quotes"))
print('I am a string with double quotes')
print(type('I am a string with double quotes'))

# Booleans

print("\nBooleans: ")
print(True)
print(False)
print(type(True))
print(type(False))

# Built-in functions

print("\nBuilt-in functions: ")
print("divmod: ")
print(divmod(10, 5))

print("\nmax: ")
print(max([10, 5]))

print("\nmin: ")
print(min([10, 5]))

print("\nround: ")
print(round(1.4 + 1.2))

# Converters

print("\nConverters: ")
print(int(1.2))
print(float(3))
print(str(10))
print(type(str(10)))

# Iterables and Iterators

print("\nIterables and Iterators: ")
print(all([True, False]))
print(any([True, False]))
print(len([1, 3, 5]))
